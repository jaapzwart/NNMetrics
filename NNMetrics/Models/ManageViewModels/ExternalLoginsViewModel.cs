using Microsoft.AspNetCore.Authentication;
using Microsoft.AspNetCore.Identity;
using System.Collections.Generic;

namespace NNMetrics.Models.ManageViewModels
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   A ViewModel for the external logins. </summary>
    ///
    /// <remarks>   Administrator, 12/06/2018. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public class ExternalLoginsViewModel
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the current logins. </summary>
        ///
        /// <value> The current logins. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public IList<UserLoginInfo> CurrentLogins { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the other logins. </summary>
        ///
        /// <value> The other logins. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public IList<AuthenticationScheme> OtherLogins { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets a value indicating whether the remove button is shown. </summary>
        ///
        /// <value> True if show remove button, false if not. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public bool ShowRemoveButton { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets a message describing the status. </summary>
        ///
        /// <value> A message describing the status. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string StatusMessage { get; set; }
    }
}