using System.ComponentModel;
using System.ComponentModel.DataAnnotations;

namespace NNMetrics.Models.ManageViewModels
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   A ViewModel for the enable authenticator. </summary>
    ///
    /// <remarks>   Administrator, 12/06/2018. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public class EnableAuthenticatorViewModel
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the code. </summary>
        ///
        /// <value> The code. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        [Required]
        [StringLength(7, ErrorMessage = "The {0} must be at least {2} and at max {1} characters long.", MinimumLength = 6)]
        [DataType(DataType.Text)]
        [Display(Name = "Verification Code")]
        public string Code { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the shared key. </summary>
        ///
        /// <value> The shared key. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        [ReadOnly(true)]
        public string SharedKey { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets URI of the authenticator. </summary>
        ///
        /// <value> The authenticator URI. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string AuthenticatorUri { get; set; }
    }
}